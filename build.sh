#!/usr/bin/env bash

for D in *; do
    if [ -d "${D}" ]; then
        for e in es-elasticsuite php-5.4-apache php-5.6-apache php-5.6-fpm php-7.0-apache php-7.0-fpm php-7.1-fpm php-7.2-apache; do
            if [[ $e == $D ]]; then
                continue 2
            fi
        done
        echo
        echo "---------------------------------------------------------------------------------------------------------"
        echo "build ${D}"
        echo "---------------------------------------------------------------------------------------------------------"
        echo
        docker build --compress -t aamant/${D} ${D}
        echo
        echo "---------------------------------------------------------------------------------------------------------"
        echo "Push ${D}"
        echo "---------------------------------------------------------------------------------------------------------"
        echo
        docker push aamant/${D}
        echo
        echo "---------------------------------------------------------------------------------------------------------"
        echo
    fi
done